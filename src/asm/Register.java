    private void Registerbutton(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Registerbutton
        // TODO add your handling code here: 
    String userName = jTextField1.getText().trim();
    String password = jTextField3.getText().trim();
    String email = jTextField2.getText().trim();
    // Check if email has '@'
    if (!email.contains("@")) {
        JOptionPane.showMessageDialog(null, "Invalid email address", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try (PrintWriter writer = new PrintWriter(new FileWriter("Account.txt", true))) {
        writer.println(userName + "," + password + "," + email);
        JOptionPane.showMessageDialog(null, "Account registered successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error registering account", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_Registerbutton

